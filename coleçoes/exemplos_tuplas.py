#Coleção Imutável - não permite alterações
tupla = ("Goku", "Vegeta", "Trunks", "Gohan")
print(tupla)

# Slicing de Elementos
print(tupla[1:3]) #Vegeta e Trunks
print(tupla[2:]) #Trunks e Gohan -> todos os elementos a frente do primeiro argumento
print(tupla[-2]) #Trunks
print(tupla[-1]) #Gohan

#Funções pre-prontas python
print(len(tupla)) # tamanho da tupla
print(max(tupla)) # aplicar em texto critério ordem alfabética
print(min(tupla)) # aplicar em texto critério ordem alfabética

